# Lista
pessoas = ["Everaldo", "Edson", "Eduardo"]

# Adiciona um item no final da lista
pessoas.append("Edson")

# .insert - adiciona um item na posição i da listqa
pessoas.insert(1, "Elias")

# .remove - remove um item da lista
pessoas.remove("Everaldo")
# .sort - Ordena a lista de acordo com a condição atribuida
pessoas.sort()
# .count - Quantidade de elementos
qtde = pessoas.count("Eduardo")
print(qtde)
# len -  quantos elementos tem na lista
len(pessoas)

# .clear - limpa a lista
pessoas.clear()




print(pessoas)
